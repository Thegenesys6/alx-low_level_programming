#include "3-calc.h"

/**
 * op_add - this calculates sum of 2 integers
 * @a- integer 1
 * @b- integer 2
 *
 * Return: sum a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - this calculates difference of 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: difference a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - this calculates product of 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - this calculates division of 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: result of the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - this calculates the remainder of division of two integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: remainder of a divided b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
